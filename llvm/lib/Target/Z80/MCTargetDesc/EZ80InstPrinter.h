#ifndef LLVM_LIB_TARGET_Z80_INSTPRINTER_EZ80INSTPRINTER_H
#define LLVM_LIB_TARGET_Z80_INSTPRINTER_EZ80INSTPRINTER_H

#include "Z80InstPrinterCommon.h"

namespace llvm {
  class Z80EInstPrinter final : public Z80InstPrinterCommon {
  public:
    Z80EInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                    const MCRegisterInfo &MRI)
        : Z80InstPrinterCommon(MAI, MII, MRI) {}

    // Autogenerated by tblgen.
    void printInstruction(const MCInst *MI, uint64_t Address,
                          raw_ostream &OS) override;
    StringRef getRegName(unsigned RegNo) const override {
      return getRegisterName(RegNo);
    }
    static const char *getRegisterName(unsigned RegNo);
  };
}

#endif
